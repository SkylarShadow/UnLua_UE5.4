---@field public StaticVariablesWritten TArray<FNiagaraVariable> @List of all attributes explicitly written by this VM script graph. Used to verify external dependencies.
---@field public ErrorMsg string
---@field public LastCompileEvents TArray<FNiagaraCompileEvent> @Array of all compile events generated last time the script was compiled.
